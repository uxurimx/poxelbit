/* ============================================
   applyLang — apply translations to DOM
   ============================================ */
function applyLang(lang) {
  const t = TRANSLATIONS[lang] || TRANSLATIONS.es;

  // Update html lang attribute and persist
  document.documentElement.setAttribute('lang', lang === 'en' ? 'en' : 'es');
  localStorage.setItem('pbLang', lang);

  // Update toggle button label (show what you'll switch TO)
  const btn = document.getElementById('lang-toggle');
  if (btn) btn.textContent = lang === 'es' ? 'EN' : 'ES';

  // Plain text
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const val = t[el.dataset.i18n];
    if (val !== undefined) el.textContent = val;
  });

  // Inner HTML (h1/h2 with nested spans)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const val = t[el.dataset.i18nHtml];
    if (val !== undefined) el.innerHTML = val;
  });

  // Placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const val = t[el.dataset.i18nPlaceholder];
    if (val !== undefined) el.placeholder = val;
  });

  // aria-label
  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    const val = t[el.dataset.i18nAria];
    if (val !== undefined) el.setAttribute('aria-label', val);
  });
}
